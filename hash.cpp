#include <stdio.h>
#include <stdint.h>
#include "crc16.h"
#include "crc32.h"
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
int main(int argc, char* argv[])
{
	crc16_t crc16;
	crc32_t crc32;


	int rc;
	uint8_t ch;
	for (;;)
	{
		rc = getchar();
		if (rc == EOF) break;

		ch = (uint8_t)rc;
		crc16.update(&ch, sizeof(ch));
		crc32.update(&ch, sizeof(ch));
	}


	printf("crc16: 0x%04lx\n", crc16.get());
	printf("crc32: 0x%08lx\n", crc32.get());


	return 0;
}
